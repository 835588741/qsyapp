package com.manyu.videoshare.bean;

public class AnalysisTimeBean {

    /**
     * code : 200
     * msg : 请求成功
     * data : 32
     * timestamp : 1588744201
     */

    private int code;
    private String msg;
    private int data;
    private int timestamp;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public int getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(int timestamp) {
        this.timestamp = timestamp;
    }
}
